    private void btn_kaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kaydetActionPerformed
        String tc=tf_tc.getText();
        String soyad=tf_soyadi.getText();
        String ad=tf_adi.getText();
        double maas=Double.parseDouble(tf_maasi.getText());
        int personel_no=Integer.parseInt(tf_personelno.getText());
        String giris=tf_girist.getText();
        double sigorta=Double.parseDouble(tf_sigorta.getText());
        String cikis=tf_cikist.getText(); 
        int teknik_servis_no=Integer.parseInt(tf_teknik_servis_no.getText());
        try {
            sonuc.moveToInsertRow();
            sonuc.updateString("personel_tc",tc);
            sonuc.updateString("personel_adi",ad);
            sonuc.updateString("personel_soyadi", soyad);
            sonuc.updateDouble("personel_maas",maas);
            sonuc.updateInt("personel_id",personel_no);
            sonuc.updateString("ise_giris_tarihi", giris);
            sonuc.updateString("cikis_tarihi",cikis);
            sonuc.updateDouble("sigorta",sigorta);
            sonuc.updateInt("teknik_servis_id", teknik_servis_no);   
            sonuc.insertRow();
            verileriGetir();
           // verileriGoster();
            } catch (SQLException e) {
                e.printStackTrace();
        }
    }//GEN-LAST:event_btn_kaydetActionPerformed

