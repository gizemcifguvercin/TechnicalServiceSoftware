    private void btn_kaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kaydetActionPerformed
        double tl=Double.parseDouble(tf_tl.getText());
        double dolar=Double.parseDouble(tf_dolar.getText());
        double mark=Double.parseDouble(tf_mark.getText());
        int kayitno=Integer.parseInt(tf_kayitno.getText());
        String tarih=tf_tarih.getText();
        try {
            sonuc.moveToInsertRow();
            sonuc.updateDouble("tl",tl);
            sonuc.updateDouble("mark",mark);
            sonuc.updateDouble("dolar", dolar);
            sonuc.updateString("tarih",tarih);
            sonuc.updateInt("dovizk_id",kayitno);   
            sonuc.insertRow();
            verileriGetir();
           // verileriGoster();
            } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_kaydetActionPerformed

