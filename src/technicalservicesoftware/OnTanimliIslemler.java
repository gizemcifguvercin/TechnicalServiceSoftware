    private void verileriGetir(){
        String sql="SELECT * FROM \"OnTanimliIsler\"";
        try {
            baglanti=dao.getBaglanti();
            ifade=baglanti.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_UPDATABLE);
            sonuc=ifade.executeQuery(sql);
            sonuc.next();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    public String kod_ata(int is_id,int teknik_servis_no){
        return "T"+ teknik_servis_no +"I"+is_id;
    }
    private void verileriGoster(){
        txtBosalt();
        if (sonuc!=null){
            try {
                tf_ad.setText(sonuc.getString("on_tanimli_is_adi")); 
                tf_kod.setText(sonuc.getString("on_tanimli_is_kodu"));
                tf_ucret.setText(sonuc.getString("on_tanimli_is_ucret"));
                tf_isno.setText(sonuc.getString("on_tanimli_is_id"));
                tf_teknikservisno.setText(sonuc.getString("teknik_servis_id"));
            } catch (Exception ex) {
                ex.printStackTrace();
            }
           
            
        }
    }
    
    private void txtBosalt(){
        tf_ad.setText("");
        tf_isno.setText("");
        tf_ucret.setText("");
        tf_kod.setText("");
        tf_teknikservisno.setText("");
        tf_ad.requestFocus();
    }
    private void ilk_islemler(){
        tf_ad.requestFocus();
        tf_teknikservisno.setText(teknik_default);
        tf_isno.setText(String.valueOf(kacinciIs()+1));
        String kod=kod_ata(Integer.parseInt(tf_isno.getText()),Integer.parseInt(tf_teknikservisno.getText()));
        tf_kod.setText(kod);
        verileriGetir();
    }
    public int kacinciIs(){
            int row=0;
            try {
                baglanti=dao.getBaglanti();
                ifade=baglanti.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_UPDATABLE);
                sonuc=ifade.executeQuery("SELECT * FROM \"OnTanimliIsler\"");
                if(sonuc!=null){
                    sonuc.last();
                    row=sonuc.getRow();
                    sonuc.beforeFirst(); //imlec basa alınır
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            return row;
        }
    
