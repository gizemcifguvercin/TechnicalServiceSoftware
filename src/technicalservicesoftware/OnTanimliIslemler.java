    private void btn_kaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kaydetActionPerformed
        String ad=tf_ad.getText();
        int is_no=Integer.parseInt(tf_isno.getText());
        String kod=tf_kod.getText();
        int teknik_servis_no=Integer.parseInt(tf_teknikservisno.getText());
        int ucret=Integer.parseInt(tf_ucret.getText());

        try {
            sonuc.moveToInsertRow();
            sonuc.updateInt("on_tanimli_is_id", is_no);
            sonuc.updateString("on_tanimli_is_adi", ad);
            sonuc.updateString("on_tanimli_is_kodu",kod);
            sonuc.updateDouble("on_tanimli_is_ucret", ucret);
            sonuc.updateInt("teknik_servis_id", teknik_servis_no);
            sonuc.insertRow();
            verileriGetir();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_kaydetActionPerformed

